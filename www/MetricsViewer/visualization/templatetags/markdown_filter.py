import markdown
from django import template

register = template.Library()


@register.filter
def markdownify(text):
    '''
    safe_mode governs how the function handles raw HTML
    However, safe_mode have been deprecated, and since all our markdown is safe in the sense that it is static content
    we can skip the safe_mode parsing.
    return markdown.markdown(text, safe_mode='escape')
    '''
    return markdown.markdown(text, extensions=['markdown.extensions.toc'])
